public class App {
    public static void main(String[] args) throws Exception {
        
        boolean a = true;
        boolean b = false;

        System.out.println(a && b); //and false
        System.out.println(a || b); //or true
        System.out.println(!a); //not a = false
        System.out.println(!b); //not b = true
    }
}
